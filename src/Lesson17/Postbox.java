package Lesson17;

public class Postbox {
    int height;
    int lenth;
    int width;
    int weight;
    public Postbox(int height, int lenth, int width, int weight){
        this.height=height;
        this.lenth=lenth;
        this.width=width;
        this.weight=weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getLenth() {
        return lenth;
    }

    public void setLenth(int lenth) {
        this.lenth = lenth;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }



}
